const DEFAULT_LANG = "zh-CN";
const LANG_ORDER = ["zh-CN", "zh-TW", "en"];
const LANG_LABEL = {
    "zh-CN": "简",
    "zh-TW": "繁",
    en: "EN"
};

function getInitialLanguage() {
    const saved = window.localStorage.getItem("site-lang") || window.localStorage.getItem("texture-lang");
    if (saved && TEXTURE_I18N[saved]) return saved;
    return DEFAULT_LANG;
}

function applyLanguage(lang) {
    const dict = TEXTURE_I18N[lang] || TEXTURE_I18N[DEFAULT_LANG];
    document.documentElement.lang = lang;

    document.querySelectorAll("[data-i18n]").forEach((node) => {
        const key = node.dataset.i18n;
        if (dict[key]) node.textContent = dict[key];
    });

    document.querySelectorAll(".language-tabs__button").forEach((button) => {
        button.classList.toggle("is-active", button.dataset.lang === lang);
    });

    const cycle = document.getElementById("languageCycle");
    if (cycle) cycle.textContent = LANG_LABEL[lang] || LANG_LABEL[DEFAULT_LANG];
    document.title = dict["meta.title"];
    window.localStorage.setItem("site-lang", lang);
    window.localStorage.setItem("texture-lang", lang);
}

function showToast(message) {
    const toast = document.getElementById("toast");
    if (!toast) return;
    toast.textContent = message;
    toast.classList.add("is-visible");
    window.clearTimeout(showToast.timer);
    showToast.timer = window.setTimeout(() => toast.classList.remove("is-visible"), 1800);
}

function setupProfileMenu() {
    const menu = document.getElementById("profileMenu");
    const trigger = document.getElementById("profileTrigger");
    const panel = document.getElementById("profilePanel");
    if (!menu || !trigger || !panel) return;

    trigger.addEventListener("click", () => {
        const open = menu.classList.toggle("is-open");
        trigger.setAttribute("aria-expanded", String(open));
        panel.hidden = !open;
    });

    document.addEventListener("click", (event) => {
        if (menu.contains(event.target)) return;
        menu.classList.remove("is-open");
        trigger.setAttribute("aria-expanded", "false");
        panel.hidden = true;
    });
}

function setupPromptCopy() {
    document.querySelectorAll("[data-copy-prompt]").forEach((button) => {
        button.addEventListener("click", async () => {
            const lang = window.localStorage.getItem("texture-lang") || DEFAULT_LANG;
            const dict = TEXTURE_I18N[lang] || TEXTURE_I18N[DEFAULT_LANG];
            const target = button.dataset.copyPrompt === "zh" ? "texturePromptZH" : "texturePromptEN";
            const prompt = document.getElementById(target);
            if (!prompt) return;

            try {
                await navigator.clipboard.writeText(prompt.textContent.trim());
                showToast(dict["toast.copy"]);
            } catch (error) {
                showToast(prompt.textContent.trim());
            }
        });
    });
}

function setupLanguageCycle() {
    const cycle = document.getElementById("languageCycle");
    if (!cycle) return;

    cycle.addEventListener("click", () => {
        const current = getInitialLanguage();
        const index = LANG_ORDER.indexOf(current);
        const next = LANG_ORDER[(index + 1) % LANG_ORDER.length] || DEFAULT_LANG;
        applyLanguage(next);
    });
}

function setupPromptTabs() {
    const tabs = document.querySelectorAll("[data-prompt-tab]");
    const panels = document.querySelectorAll("[data-prompt-panel]");
    if (!tabs.length || !panels.length) return;

    tabs.forEach((tab) => {
        tab.addEventListener("click", () => {
            const lang = tab.dataset.promptTab;
            tabs.forEach((item) => {
                const active = item === tab;
                item.classList.toggle("is-active", active);
                item.setAttribute("aria-selected", String(active));
            });
            panels.forEach((panel) => {
                panel.hidden = panel.dataset.promptPanel !== lang;
            });
        });
    });
}

function setupImagePreview() {
    const preview = document.getElementById("imagePreview");
    const image = document.getElementById("imagePreviewImg");
    const title = document.getElementById("imagePreviewTitle");
    if (!preview || !image || !title) return;

    const close = () => {
        preview.hidden = true;
        image.src = "";
        document.body.style.overflow = "";
    };

    document.querySelectorAll("[data-preview-src]").forEach((button) => {
        button.addEventListener("click", () => {
            image.src = button.dataset.previewSrc;
            image.alt = button.dataset.previewTitle || "";
            title.textContent = button.dataset.previewTitle || "";
            preview.hidden = false;
            document.body.style.overflow = "hidden";
        });
    });

    preview.querySelectorAll("[data-preview-close]").forEach((button) => {
        button.addEventListener("click", close);
    });

    document.addEventListener("keydown", (event) => {
        if (event.key === "Escape" && !preview.hidden) close();
    });
}

document.addEventListener("DOMContentLoaded", () => {
    setupLanguageCycle();
    setupProfileMenu();
    setupPromptCopy();
    setupPromptTabs();
    setupImagePreview();

    document.querySelectorAll(".language-tabs__button").forEach((button) => {
        button.addEventListener("click", () => applyLanguage(button.dataset.lang));
    });

    applyLanguage(getInitialLanguage());
});
